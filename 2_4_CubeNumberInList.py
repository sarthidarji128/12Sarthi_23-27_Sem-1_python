# Create a list of numbers
c = [2, 3, 4, 5, 6, 7, 8, 9]

# Create a new list with the cubes of the numbers using list comprehension
cube = [(num**3) for num in c]  # Calculate the cube of each number in c and create a new list

# Print the original list
print("Original list:")
print(c)

# Print the list of cubes
print("\nList of cubes:")
print(cube)
